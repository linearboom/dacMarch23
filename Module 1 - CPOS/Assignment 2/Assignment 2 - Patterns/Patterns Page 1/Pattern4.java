class Pattern4 {
	public static void main(String args[]){
		for (int i = 1 ;  i <= 5; i++){
			for (char j = 1 ; j<=i; j++){
				System.out.print(i+ " ");
			}
			System.out.println("");
		}
		//System.out.println("Done");
		// Instead of 'E' we can also write the ASCII value
		// ASCI of E = 69
		
	}
	
}