class Pattern2 {
	public static void main(String args[]){
		for (char i = 'A' ;  i <= 'E'; i++){
			for (char j = 'A' ; j<=i; j++){
				System.out.print(j+" ");
			}
			System.out.println("");
		}
		//System.out.println("Done");
		// Instead of 'E' we can also write the ASCII value
		// ASCI of E = 69
		
	}
	
}